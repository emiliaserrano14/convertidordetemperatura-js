// preguntar la temperatura en celsius 
let temperatura =  parseInt(prompt("cual es la temperatura en celsius?"));
// convertir celsius a fahrenheit
let convertToFahrenheit = (temperatura*1.8) + 32;
// mostrar resultado en la consola 
console.log(`la temperatura en fahrenheit es ${convertToFahrenheit}`)
// mostrar en la web
document.write(`la temperatura en fahrenheit es ${convertToFahrenheit}`)

